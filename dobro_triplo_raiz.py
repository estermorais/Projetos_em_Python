#Crie um algoritmo que leia um número e mostre o seu dobro
#o seu triplo e a sua raiz quadrada

n = float(input('Digite um número: '))

print('O dobro do número digitado é {}' .format((n*2)))
print('O triplo do número digitado é {}' .format((n*3)))
print('A raiz do número digitado é {}' .format((n**(1/2))))